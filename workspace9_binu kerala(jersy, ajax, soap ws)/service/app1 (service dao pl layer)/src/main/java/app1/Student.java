package app1;

public class Student {

	private int roll;
	private String  name;
	private Birthday bday;
	
	public int getRoll() {
		return roll;
	}
	public void setRoll(int roll) {
		this.roll = roll;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Birthday getBday() {
		return bday;
	}
	public void setBday(Birthday bday) {
		this.bday = bday;
	}
	
	
	
}
