



public class Program2 {

	static void alphabet(){
		for(int x=65;x<91;x++)
			 System.out.print((char)x+" \t");
	}
	
	static void alphabet1(){
		for(char x='a';x<='z';x++)
			 System.out.print(x+" \t");
	}

	
	
	public static void main(String[] args) {
		alphabet();
		System.out.println();
		alphabet1();
	}
	
}
