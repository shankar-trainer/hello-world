
public class IfProgram1 {

	public static void main(String[] args) {
		int x=10;
		
		System.out.println(x%2);
		System.out.println(15%2);
		
		System.out.println(15/2);
		
		System.out.println(19%4);
		System.out.println(19/4);
		
		
		System.out.println(x%2==0);
		System.out.println(x%2!=0);
		
		//if()
	}
}
