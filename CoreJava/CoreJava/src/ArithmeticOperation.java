
public class ArithmeticOperation {

	public static void main(String[] args) {
		
		int x=1;
		x=x+1;
		System.out.println(x);
		x+=1;
		System.out.println(x);
		x++;
		System.out.println(x);
		
		System.out.println(x++);  // post increment 
		System.out.println(x);
		
		int y=1;
		System.out.println(++y);
		System.out.println(++y);
		
		
	}
}
