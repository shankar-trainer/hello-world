
public class ForLoop {

	public static void main(String[] args) {
		/*
		 * int counter=1;
		 * 
		 * while(counter<=10) { 
		 * System.out.println("count "+counter); 
		 * counter=counter+1;
		 * }
		 */
		
		for (int counter = 1; counter <= 10; counter++) {
			System.out.println(counter);
		}

	}
}
