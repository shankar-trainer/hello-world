
public class WhileLoop {

	public static void main(String[] args) {
		int counter=1;
		
		while(counter<=10) {
			System.out.println("count "+counter);
			counter=counter+1;
		}
		System.out.println("====================");
		int c=10;
		
		while(c>=1) {
			System.out.println("count "+c);
			c=c-1;
		}
	}
}
