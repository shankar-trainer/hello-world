
public class Program3 {

	public static void main(String[] args) {
		
		int x=100;   //decimal number 
		int y=0b10010;// binary no 01,
		int z=0x123aef;// hexa decimal no 0 -9 a-f 
		int a=01237;   // octal no 0-7     
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(a);
		
		
		// binary prefix 0b
		// hexadecimal prefix 0x
		// octal prefix 0
		// decimal  -- no prefix 
		
	}
}
