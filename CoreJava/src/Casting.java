
public class Casting {

	public static void main(String[] args) {
		 byte b1=56;
		 int x=b1;  // implicit casting  or widening 
		 double d1=x; // implicit casting 

		 double s1=6676776766565.4455;
		 
		 int c1=(int) s1;// explicit  casting or narrowing 
		 System.out.println(s1+"  \n\n "+c1);
		 
	}
}
