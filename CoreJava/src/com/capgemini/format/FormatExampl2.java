package com.capgemini.format;

public class FormatExampl2 {

	public static void main(String[] args) {
		float salary = 56000.56777f;

		System.out.printf("salary is  %n %.2f", salary);
		
		char c='a';
		int x=1000;
		String s="hello";
		
		System.out.printf("%n%s",s);
		System.out.printf("%n%d",x);
		System.out.printf("%n%c",c);
		
		
	}
}
