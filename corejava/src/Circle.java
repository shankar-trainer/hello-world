
public class Circle {

	
	public static void main(String[] args) {
		
		float radius=34.5f;
		float area=3.14f*radius*radius;
		float circumference=2*3.14f*radius;
		
		System.out.println("Radius is "+radius+"\nArea : "+area+"\nCircumference :"+circumference);
		
		
		System.out.printf("\nRadius is %f\nArea is %f\nCircumference :%f",radius,area,circumference);
	}
	
}
