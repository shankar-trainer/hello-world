public class Medicine {

	private int id;
	private String name;
	private float cost;
	
	public Medicine(int id, String name, float cost) {
		super();
		this.id = id;
		this.name = name;
		this.cost = cost;
	}

	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public float getCost() {
		return cost;
	}

	 
}
