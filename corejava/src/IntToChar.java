
public class IntToChar {

	public static void main(String[] args) {
		int x=122;
		
		System.out.println(x+"  "+(char)x);
		System.out.println(97+"  "+(char)97);

		System.out.println(65+"  "+(char)65);
		System.out.println(90+"  "+(char)90);
		
		char c='z';
		System.out.println(c+"  "+(int)c);
		
		
		
		
		
	}
}
