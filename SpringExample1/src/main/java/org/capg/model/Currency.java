package org.capg.model;

public class Currency {
	private float rupees;
	private float dollar;

	public float getRupees() {
		return rupees;
	}

	public void setRupees(float rupees) {
		this.rupees = rupees;
	}

	public float getDollar() {
		return dollar;
	}

	public void setDollar(float dollar) {
		this.dollar = dollar;
	}

}
