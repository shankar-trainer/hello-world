package com.pathinfotech.model;

public class Department {
	private int departmentId;
	private String departmentName;
	private int departmentNoOfEmployee;

	public int getDepartmentId() {
		return departmentId;
	}

	public void setDepartmentId(int departmentId) {
		this.departmentId = departmentId;
	}

	public String getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	public int getDepartmentNoOfEmployee() {
		return departmentNoOfEmployee;
	}

	public void setDepartmentNoOfEmployee(int departmentNoOfEmployee) {
		this.departmentNoOfEmployee = departmentNoOfEmployee;
	}

}
