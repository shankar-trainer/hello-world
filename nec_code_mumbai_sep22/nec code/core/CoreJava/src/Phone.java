
public class Phone {

	private long officePhone;
	private long homePhone;
	public Phone(int officePhone, int homePhone) {
		super();
		this.officePhone = officePhone;
		this.homePhone = homePhone;
	}
	public long getOfficePhone() {
		return officePhone;
	}
	public long getHomePhone() {
		return homePhone;
	}
	
	
}
