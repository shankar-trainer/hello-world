
public class EvenOrOdd {

	// main ctrl +space
	public static void main(String[] args) {
		int x = 13;
		System.out.println(x / 2);
		System.out.println(x % 2); // % -- give you reminder
		if (x % 2 == 0)
			System.out.println(x + " is even no ");
		else
			System.out.println(x + " is odd no ");
	}
}
