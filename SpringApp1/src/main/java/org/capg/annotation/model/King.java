package org.capg.annotation.model;

public class King {

	private String kingName;
	private String kingCountry;
	private String kingCapital;
	
	public String getKingName() {
		return kingName;
	}
	public void setKingName(String kingName) {
		this.kingName = kingName;
	}
	public String getKingCountry() {
		return kingCountry;
	}
	public void setKingCountry(String kingCountry) {
		this.kingCountry = kingCountry;
	}
	public String getKingCapital() {
		return kingCapital;
	}
	public void setKingCapital(String kingCapital) {
		this.kingCapital = kingCapital;
	}

	
}
