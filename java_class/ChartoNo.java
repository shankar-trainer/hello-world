class CharToNo{
	public static void main(String args[]){
		
		int i=65;
		System.out.println(i);// 65 
		System.out.println((char)i);// A
			
		i=66;
		
		System.out.println((char)i);// B
		
		i=67;
		System.out.println((char)i);// C
		
		char a='G';
		System.out.println(a);// G
		System.out.println((int)a);//71 
		
       		
	}
	
}


