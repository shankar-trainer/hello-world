class CapitalLetter {
	
	
	public static void main(String args[]){
		
		// 65 --- A
		//66   --- B 
		//90   --- Z 
	System.out.println("********** Capital Letter ********\n");
		for(int x=65;x<=90;x++){
			System.out.print((char)x+"\t");
		}
	
    System.out.println("\n********** Capital Letter ********\n");
	
	    for(char  x='A';x<='Z';x++){
			System.out.print(x+"\t");
		}
		
	}
	
}